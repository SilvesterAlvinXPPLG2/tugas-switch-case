let nilai = 67;

switch (true) {
    case (nilai >= 91):
        console.log("A");
        break;
    case (nilai >= 81):
        console.log("B");
        break;
    case (nilai >= 71):
        console.log("C");
        break;    
    case (nilai >= 61):
        console.log("D");
        break;
    default:
        console.log("E");
        break;
}


